def process_data(madlibs):
    street=madlibs["street"]
    animal=madlibs["animal"]
    verb=madlibs["verb"]
    clothing=madlibs["clothing"]
    clothing_plural=madlibs["clothing_plural"]
    color=madlibs["color"]
    vehicle=madlibs["vehicle"]
    place=madlibs["place"]
    plural_animal=madlibs["plural_animal"]
    people=madlibs["people"]
    return(street,animal,verb,clothing,clothing_plural,color,vehicle, place,plural_animal,people)